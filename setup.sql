-- 1. إنشاء جدول المنتجات (products)
CREATE TABLE IF NOT EXISTS public.products (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    title TEXT NOT NULL,
    category TEXT NOT NULL, -- الفئات: (ماندالا، ساعات حائط، جداريات طبيعة، تجريد وهندسة، خط عربي وإسلامي)
    price NUMERIC NOT NULL,
    original_price NUMERIC,
    specifications TEXT,
    description TEXT,
    image_url TEXT,
    is_available BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. إنشاء جدول الحجوزات والطلبات (bookings)
CREATE TABLE IF NOT EXISTS public.bookings (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    customer_name TEXT NOT NULL,
    whatsapp TEXT NOT NULL,
    governorate TEXT NOT NULL,
    product_title TEXT NOT NULL,
    notes TEXT,
    status TEXT DEFAULT 'جديد' NOT NULL, -- الحالات: (جديد، قيد التأكيد، تم الشحن، تم الاستلام، ملغي)
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 3. تفعيل الحماية على مستوى الصفوف (Row Level Security - RLS)
ALTER TABLE public.products ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.bookings ENABLE ROW LEVEL SECURITY;

-- 4. سياسات الوصول العام (Public Policies)
-- السماح للعامة (الزوار) باستعراض المنتجات فقط
CREATE POLICY "Allow public read access to products" ON public.products
    FOR SELECT USING (true);

-- السماح للعامة (الزوار) بإدخال طلبات حجز جديدة فقط
CREATE POLICY "Allow public insert access to bookings" ON public.bookings
    FOR INSERT WITH CHECK (true);

-- 5. سياسات الوصول للمسؤولين المسجلين (Admin Policies)
-- السماح للمسؤولين المسجلين (Authenticated) بالتحكم الكامل بالمنتجات (إضافة/تعديل/حذف)
CREATE POLICY "Allow authenticated users full access to products" ON public.products
    USING (auth.role() = 'authenticated');

-- السماح للمسؤولين المسجلين (Authenticated) بالتحكم الكامل بالحجوزات (عرض/تعديل حالة/حذف)
CREATE POLICY "Allow authenticated users full access to bookings" ON public.bookings
    USING (auth.role() = 'authenticated');

